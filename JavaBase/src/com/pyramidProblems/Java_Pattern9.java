package com.pyramidProblems;

public class Java_Pattern9 {

	public static void main(String[] args) {
		for(int i=1 ;i<=11; i++)
		{
			for(int j=0; j<i; j++)
			{
				System.out.print("*");
			}
			System.out.println("");
		}
		
		for(int i=11; i>0; i--)
		{
			for(int j=0; j<i; j++)
			{
				System.out.print("*");
			}
			System.out.println("");
		}
		
		
		

	}

}
